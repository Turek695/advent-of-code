function xmasFound(row, col, word, directions, minWords = 1) {
    let wordNumber = 0;
    if (inputTable[row][col] !== word[0]) {
        return wordNumber;
    }
    for (const [r, c] of directions) {
        wordNumber += findInDirection(r, c);
    }
    return wordNumber >= minWords ? wordNumber : 0;
    function findInDirection(rowDir, colDir) {
        let rowCurrent = row;
        let colCurrent = col;
        for (const letter of word) {
            if (inputTable[rowCurrent] === undefined
                || inputTable[rowCurrent][colCurrent] !== letter) {
                return 0;
            }
            rowCurrent += rowDir;
            colCurrent += colDir;
        }
        return 1;
    }
}
function masFound(rowCenter, colCenter, word) {
    const isCenter = inputTable[rowCenter][colCenter] === word[1];
    if (!isCenter) {
        return 0;
    }
    const diagonalLetters = {
        leftTop: inputTable[rowCenter - 1][colCenter - 1],
        rightBottom: inputTable[rowCenter + 1][colCenter + 1],
        rightTop: inputTable[rowCenter + 1][colCenter - 1],
        leftBottom: inputTable[rowCenter - 1][colCenter + 1],
    };
    const isProperLetters = Object.values(diagonalLetters)
        .every(el => [word[0], word[2]].includes(el));
    if (!isProperLetters) {
        return 0;
    }
    const isSame = diagonalLetters.leftTop === diagonalLetters.rightBottom
        || diagonalLetters.rightTop === diagonalLetters.leftBottom;
    if (isSame) {
        return 0;
    }
    return 1;
}
